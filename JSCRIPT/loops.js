document.write("For Loop:<br>");
for (let i = 1; i <= 5; i++) {
  document.write(i + "<br>");
}

document.write("<br>");

// for...of loop
document.write("For...of Loop:<br>");
let colors = ["Red", "Green", "Blue"];

for (let color of colors) {
  document.write(color + "<br>");
}

document.write("<br>");

// while loop
document.write("While Loop:<br>");
let j = 1;

while (j <= 3) {
  document.write(j + "<br>");
  j++;
}
